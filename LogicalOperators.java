package com.vasdoltechnologies;

public class LogicalOperators {

	public static void main(String[] args) {
		//logical operators
		int a=200;
		int b=150;
		int c=100;
		
		System.out.println(a>b && b>c);
		
		System.out.println(a<b && c<a);
		
		System.out.println(a<b ||b>c);
		
	}

}

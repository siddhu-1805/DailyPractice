package com.vasdoltechnologies;

public class AssignmentOperator {

	public static void main(String[] args) {
		//Assignment Operators
		int a = 23;
		int b =18;
		System.out.println(a+=b);
		System.out.println(a-=b);
		System.out.println(a*=b);
		System.out.println(a/=b);
		System.out.println(a%=b);
		System.out.println(a&=b);
		System.out.println(a|=b);
		System.out.println(a^=b);
	}

}

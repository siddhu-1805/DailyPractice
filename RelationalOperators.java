package com.vasdoltechnologies;

public class RelationalOperators {

	public static void main(String[] args) {
		//RelationalOperators
		int a=30;
		int b=20;
		
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a!=b);
		System.out.println(a==b);
	}

}
